void multiplyMatrix(int m1[][C1], int m2[][C2])
{
	int result[R1][C2];

	//printf("Resultant Matrix is:\n\r");


	for (int i = 0; i < R1; i++) {
		for (int j = 0; j < C2; j++) {
			result[i][j] = 0;

			for (int k = 0; k < R2; k++) {
				result[i][j] += m1[i][k] * m2[k][j];
			}
			//HAL_Delay(50);
			//printf("%d",result[i][j]);
		}

		//printf("\n\r");
	}
}

