		//arm_status status = arm_mat_mult_q31(&matA, &matB, &matOut);
		arm_mat_mult_q31(&matA, &matB, &matOut);
		cycles = DWT->CYCCNT;
		time = ((float)cycles / SystemCoreClock) * 1000.0;//milli seconds
		printf("Time CMSIS: %f msec, Cycles: %lu\r\n", time, cycles);
